package storage;

/**
 * Διεπαφή που εξασφαλίζει ότι τα αντικείμενα μπορούν να μετατραπούν 
 * σε μορφή CSV για αποθήκευση και να ανακτηθούν από αυτή.
 */
public interface Storable {
    
    /**
     * Επιστρέφει την κατάσταση του αντικειμένου ως μια γραμμή CSV.
     * Π.χ.: "admin1,mysecretpassword"
     */
    String toCSV();

    /**
     * Ενημερώνει τα πεδία του αντικειμένου διαβάζοντας μια γραμμή CSV.
     * @param csvLine Η γραμμή κειμένου που διαβάστηκε από το αρχείο.
     */
    void fromCSV(String csvLine);
}